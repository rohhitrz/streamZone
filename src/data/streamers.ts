export const getFeaturedStreamer = (): Streamer => {
  const liveStreamers = streamers.filter(streamer => streamer.isLive);
  return liveStreamers.sort((a, b) => b.viewerCount - a.viewerCount)[0];
};

export const getLiveStreamers = (): Streamer[] => {
  return streamers.filter(streamer => streamer.isLive);
};

export const getStreamerById = (id: string): Streamer | undefined => {
  return streamers.find(streamer => streamer.id === id);
};

export const getRecommendedStreamers = (currentStreamerId: string): Streamer[] => {
  return streamers
    .filter(streamer => streamer.id !== currentStreamerId && streamer.isLive)
    .sort((a, b) => b.viewerCount - a.viewerCount)
    .slice(0, 4);
}; 